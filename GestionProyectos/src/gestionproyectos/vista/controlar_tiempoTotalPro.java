    public void cargarInformacion(){
    this.cont=0;
    List<Tareas> tareasProyecto = PrincipalController.getInstance().cargarTareasControlar();
    List<TareaInfo> tareasInfo = PrincipalController.getInstance().getTotasTareasInfo();
    
        for (int i = 0; i < tareasProyecto.size(); i++) {
            
            for (int j = 0; j < tareasInfo.size(); j++) {
                if(tareasProyecto.get(i).getIdTarea()==tareasInfo.get(j).getIdtarea()){
                this.cont= this.cont+tareasInfo.get(j).getTiempoTotal();
                } 
                
            }
            
            
        }
        
        
        String contText = Integer.toString(this.cont);
        TXT_TIME.setText(contText+"s");
    
    }
   
   
